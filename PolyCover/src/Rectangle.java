
public class Rectangle {

	private Points leftDown; 
	private Points rightDown;
	private Points leftUp;
	private Points rightUp;
	
	public Points getLeftDown() {
		return leftDown;
	}
	public void setLeftDown(Points leftDown) {
		this.leftDown = leftDown;
	}
	public Points getRightDown() {
		return rightDown;
	}
	public void setRightDown(Points rightDown) {
		this.rightDown = rightDown;
	}
	public Points getLeftUp() {
		return leftUp;
	}
	public void setLeftUp(Points leftUp) {
		this.leftUp = leftUp;
	}
	public Points getRightUp() {
		return rightUp;
	}
	public void setRightUp(Points rightUp) {
		this.rightUp = rightUp;
	}
	
	
}
